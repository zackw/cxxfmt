import generator
import sys

words = [ '', 'i', 'of', 'sis', 'fice', 'drisk', 'elanet', 'hippian',
          'botanist', 'synaptene', 'cipherhood', 'schizognath' ]

aligns = [ '', '<', '>', '^', 'L<', 'R>', 'C^' ]

maxw = len(words) + 3

def output(spec, val):
    spec = '{:' + spec + '}'
    return '"{}", "{}", "{}"'.format(spec, spec.format(val), val)

def g_simple():
    for r in words:
        for a in aligns:
            yield output(a, r)

def g_width():
    for r in words:
        for w in xrange(1, maxw):
            for a in aligns:
                yield output('{}{}'.format(a, w), r)

def g_prec():
    for r in words:
        for p in xrange(maxw):
            for a in aligns:
                yield output('{}.{}'.format(a, p), r)

def g_wnp():
    for r in words:
        for w in xrange(1, maxw):
            for p in xrange(maxw):
                for a in aligns:
                    yield output('{}{}.{}'.format(a, w, p), r)

