def main(argv):
    if len(argv) > 1:
        outf = open(argv[1], "w")
    else:
        outf = sys.stdout
    with outf:
        gen = generator.TestGenerator()
        gen.add_module('strings', 'case_1arg_s', globals())

        outf.write(skeleton_top)
        gen.generate(outf)
        outf.write(skeleton_bot)

if __name__ == '__main__':
    main(sys.argv)
